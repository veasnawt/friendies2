package com.example.friendies.model;

public class UserRatingsModel {

    int id, userpic;
    String user_rating, user_opinion, username;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getUserpic() {
        return userpic;
    }

    public void setUserpic(int userpic) {
        this.userpic = userpic;
    }

    public String getUser_rating() {
        return user_rating;
    }

    public void setUser_rating(String user_rating) {
        this.user_rating = user_rating;
    }

    public String getUser_opinion() {
        return user_opinion;
    }

    public void setUser_opinion(String user_opinion) {
        this.user_opinion = user_opinion;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }
}
